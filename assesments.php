<div id="pmp" class="section_header section_header_bar8">II.&nbsp;&nbsp;Progressive Mastery Programme (PMP) <span class="ace_icon_arrow"><img src="images/website/Web_down_arrow.png" id="pmp_img"/></span></div>
    <div id="pmp_content">
        <br />
        <p>
          The PMP is a powerful programme that randomly generates unlimited open-ended questions.  It contains a short explanation of key concepts, and a solution hint to guide learners.
     <br />  <br />
    PMP auto-marks and provides instant feedback.  This allows the student to reflect on any mistake made and prevent it from recurring as he/she progresses to the next question.  A comprehensive report provides information on every student's input, thereby providing teachers with a diagnostic tool to identify students who need help.  Available to students as well, the reports enable students to identify their weaknesses.
	</p>
