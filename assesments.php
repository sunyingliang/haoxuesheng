<div id="games" class="section_header section_header_bar8">III.&nbsp;&nbsp;Game-Based Assessment <span class="ace_icon_arrow"><img src="images/website/Web_down_arrow.png" id="game_img"/></span></div>
    <div id="games_content">
        <br />
        <p>
      Have Fun and Learn!
<br /><br />
Using games to enhance learning is a positive strategy that reduces "test-anxiety". Through games, students master concepts while enjoying the process.
<br /><br />
The games are designed with three levels of difficulty, and a player is given three chances to achieve the goal.  Unlike most online games which show only the player's score, the scoreboard provided displays the questions that were answered both correctly and incorrectly; thereby providing teachers with a diagnostic tool to check each student's level of understanding of the topic.  Players are also able to see their ranking among other users in the game centre. 
</p><br />
            <p class="consultant_name">Samples:</p>
            <br />
        <p class="section_subheader">
               Example 1:&nbsp;&nbsp;Monkey Rescue
        </p><br />
		<div class="lesson_imgdiv">
