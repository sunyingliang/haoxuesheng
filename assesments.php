          </div><br />
			<p>
            	This game helps a player to master the various techniques of Rounding-off Numbers.  The objective of the game is to rescue the female monkey by overcoming challenges. These challenges are questions on rounding-off numbers.  A correct answer would bring the player closer to the goal, while an incorrect one would delay the player.  The time taken to rescue the female monkey affects the bonus points awarded.  Players lose a chance when the monkey falls down the creek. 
            </p>
