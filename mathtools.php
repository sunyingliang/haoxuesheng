        <p>
        	Sketching the graph of any function is one of the most tedious activity in mathematics. To assist students and teachers, the All-in-1 Grapher was created to display properties, data, gradient and solution(s) of different function graphs.  It is programmed with the flexibility of hiding or displaying grids, coordinates and labels.  This tool is versatile, allowing individual or multiple graphs to be displayed in a single plane, which can be downloaded and printed.
