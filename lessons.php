<p class="section_subheader">
	Example 2:&nbsp;&nbsp;Distance-Time Graph
</p>
<br>
<div class="lesson_imgdiv">
	<img src="images/website/Web_Distance_Time_Graph_740.jpg" class="lesson_img shadow">
