<p>
	The lesson is designed to help students derive the volume of a sphere with a simple experiment using the Water-displacement method.  It begins with an eye-catching introduction of the world's largest helium balloon (held till September 2008) to generate interest in the topic.  At the end of the lesson, a number of examples are given to show how the formula is applied.
