<p>
With technology advancing rapidly, 21st century learners want to have fun while learning.  Making Mathematics appealing and interesting is a challenge common to all Mathematics educators.  As a partner of mathematics education, ACE-Learning, in its expertise, has designed lessons to suit the needs of a 21st century learner. 
</p><br />
<p>
The lessons are designed to cover a range of topics, aiming to help students develop their cognitive and problem solving skills.  They provide connection with day-to-day activities and continuity between different mathematics concepts.  Visual graphics and sounds enhance the lessons to aid comprehension.  Each lesson unit comes with several examples of varying difficulty.  The interface is user friendly and flexible, designed to give students the freedom to maneuver the lessons according to their individual learning pace. 
</p><br />
<p>
Downloadable Lesson Briefs outline key concepts including worked examples, giving students and teachers a chance to have a summary of topics covered in each unit.
</p>
