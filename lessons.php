<p>
The concept of a distance-time graph is illustrated with two scenarios of a bus travelling from town to town.  As the virtual clock ticks, the bus moves and the graph is plotted simultaneously.  An analysis of the distance-time graph in relation to the bus' journeys helps the students to grasp the concepts of gradient, speed and average speed. 
</p>